#ifndef SRS_HDR_B1_MEM_H
#define SRS_HDR_B1_MEM_H 1


#define HSM_B1_HSMRAM_REGION_START 0x104000
#define HSM_B1_HSMRAM_REGION_END 0x10A000

#define SCP_B1_HEAP_STACK_SIZE 0x4000
#define SCP_B1_SCP_TCMA_REGION_START 0x00000000
#define SCP_B1_SCP_TCMA_REGION_END (SCP_B1_SCP_TCMA_REGION_START + (64*1024))
#define SCP_B1_SCP_TCMB_REGION_START 0x00800000
#define SCP_B1_SCP_TCMB_IPC_START    0x00809000
#define SCP_B1_SCP_TCMB_REGION_END   (0x00800000 + (128*1024))

#define HSM_B1_SCP_TCMA_REGION_START (0x12000000 + SCP_B1_SCP_TCMA_REGION_START)
#define HSM_B1_SCP_TCMA_REGION_END (0x12000000 + SCP_B1_SCP_TCMA_REGION_END)
#define HSM_B1_SCP_TCMB_REGION_START (0x12000000 + SCP_B1_SCP_TCMB_REGION_START)
#define HSM_B1_SCP_TCMB_IPC_START  (0x12000000 + SCP_B1_SCP_TCMB_IPC_START)
#define HSM_B1_SCP_TCMB_REGION_END (0x12000000 + SCP_B1_SCP_TCMB_REGION_END)

/*! Defines the max B2 image that can be loaded by B1
 *  SRAM - 4MB
 *  SCP TCM - 384KB
 *  256KB HSM RAM
 *  128KB CCP RAM
 *  256 P5 DRAM*/
#define MAX_B2_IMG_LOAD_SIZE ((1 * 1024 * 1024) + (384 * 1024) + (256 * 1024) + (128 * 1024) + (256 * 1024))


#endif // SRS_HDR_B1_MEM_H
